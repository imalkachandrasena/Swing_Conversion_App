    private void btnconActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconActionPerformed
        if(jtxtenter.getText().equals("")||jcbox.getSelectedItem()==null||jcbox1.getSelectedItem()==null){
            JOptionPane.showMessageDialog(rootPane, "Please select convertion type or enter the value.","Information",JOptionPane.INFORMATION_MESSAGE);
        }else{
            Double value = Double.valueOf(jtxtenter.getText());
            String fromType = jcbox.getSelectedItem().toString();
            String fromTo = jcbox1.getSelectedItem().toString();

            VolumeCon converter = new VolumeCon();
            double result = converter.convertVolume(fromType, fromTo, value);
            String print = String.format("%.10f", result);
            jtxtans.setText(print);
            btnSave.setEnabled(true);
        }
    }//GEN-LAST:event_btnconActionPerformed

