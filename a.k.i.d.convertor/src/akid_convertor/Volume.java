    class VolumeCon {

        public double convertVolume(String fromVolume, String toVolume, double value) {
            double A = 0.001;
            double B = 0.000001;
            double C = 0.000000001;

            double valueInmm;
            switch (fromVolume) {
                case "mm^3":
                    valueInmm = value;
                    break;
                case "cm^3":
                    valueInmm = value / A;
                    break;
                case "L":
                    valueInmm = value / B;
                    break;
                case "m^3":
                    valueInmm = value / C;
                    break;
                case "ml":
                    valueInmm = value / A;
                    break;
                default:
                    throw new IllegalArgumentException("Conversion from " + fromVolume + " is not supported.");
            }

            double convertedValue;
            switch (toVolume) {
                case "mm^3":
                    convertedValue = valueInmm;
                    break;
                case "cm^3":
                    convertedValue = valueInmm * A;
                    break;
                case "L":
                    convertedValue = valueInmm * B;
                    break;
                case "m^3":
                    convertedValue = valueInmm * C;
                    break;
                case "ml":
                    convertedValue = valueInmm * A;
                    break;
                default:
                    throw new IllegalArgumentException("Conversion to " + toVolume + " is not supported.");
            }

            return convertedValue;
        }
    }

