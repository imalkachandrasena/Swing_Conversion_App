    class LengthCon{
    public double convertLength(String fromLength, String toLength, double value) {
        // Define exchange rates manually (replace with actual rates)
        double mmTocm = 0.1 ;
        double mmTom = 0.001 ;
        double mmTokm = 0.000001 ;
        double mmToMiles = 0.000000621 ;
        double mmToft = 0.00328 ;
        double mmToInch = 0.0393;
        
        double valueInmm;
        switch (fromLength) {
            case "mm":
                valueInmm = value;
                break;
            case "cm":
                valueInmm = value / mmTocm;
                break;
            case "m":
                valueInmm = value / mmTom;
                break;
            case "km":
                valueInmm = value / mmTokm;
                break;
            case "miles":
                valueInmm = value / mmToMiles;
                break;
            case "foot":
                valueInmm = value / mmToft;
                break;
            case "inch":
                valueInmm = value / mmToInch;
                break;
            default:
                throw new IllegalArgumentException("Conversion from " + fromLength + " is not supported.");
        }

       
        double convertedValue;
        switch (toLength) {
            case "mm":
                convertedValue = valueInmm;
                break;
            case "cm":
                convertedValue = valueInmm * mmTocm;
                break;
            case "m":
                convertedValue = valueInmm * mmTom;
                break;
            case "km":
                convertedValue = valueInmm * mmTokm;
                break;
            case "miles":
                convertedValue = valueInmm * mmToMiles;
                break;
            case "foot":
                convertedValue = valueInmm * mmToft;
                break;
            case "inch":
                convertedValue = valueInmm * mmToInch;
                break;
            default:
                throw new IllegalArgumentException("Conversion to " + toLength + " is not supported.");
        }

        return convertedValue;
    }
    }
